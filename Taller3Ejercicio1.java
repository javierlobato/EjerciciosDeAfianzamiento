public class Taller3Ejercicio1 {

    public static void main(String[] args) {
        
        
        //variables
        int i, j,k;
        
        //inicializacion de viriables
        k = 10;
        for (i=1;i<=k;i++) {
                for (j=1;j<=i;j++) {
                        System.out.print("*");
                }
                System.out.println("");
        }
	
    }
}
